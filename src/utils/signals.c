#include "minishell.h"
#include <readline/readline.h>
#include <readline/history.h>

void	sigint_handler(int sig_num)
{
	g_exit_code = 1;
	write(1, "\n", 1);
	rl_on_new_line();
	rl_replace_line("", 0);
	rl_redisplay();
	(void)sig_num;
}
